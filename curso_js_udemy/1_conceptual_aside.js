// Syntax Parses
// Um programa que lê o seu código e determina o que ele
// faz e se a sua gramática está válida
// -- o código não é uma mágica, alguém escreveu ele e um
// computador o traduziu

// Execution Contexts
// um wrapper para ajudar a gerenciar o código que está rodando
//

// Lexical Environments
// Algo que fica fisicamente escrito no seu códgo
// Lexical significa algo que tem a ver com palavra ou gramática
// um ambiente léxico existe na programação para indicar algo que
// você escreveu de importante

// Name / Value Pair:
// é um nome que mapeia para um único valor
// O nome pode ser definido mais que uma vez, mas pode ter
// apenas um valor dado seu contexto
// Ex: Address = '100 Main St'

// Object:
// é uma coleção de name value pairs
// ex:
Anddress: {
  Street: 'Main',
  Number: 100,
  Apartment: {
    Floor: 3,
    Number: 301
  }
}
